class TestFactionFilterAndDeployment:
    def test_get_units_for_faction_includes_new_variants(self):
        """get_units_for_faction() returns the new variants alongside factory units."""
        db = build_cc2_unit_database()
        for faction in Faction:
            faction_units = get_units_for_faction(faction)
            # Every returned unit must belong to the requested faction.
            assert all(u.faction == faction for u in faction_units)
            assert len(faction_units) < len(db)

        # Spot-check: US faction now includes the Easy Eight + Ranger Elite.
        us_units = get_units_for_faction(Faction.AMERICAN)
        us_ids = {u.template_id for u in us_units}
        assert "us_sherman_easy_eight" in us_ids
        assert "us_ranger_squad_elite" in us_ids
        # And the factory unit is still there.
        assert "us_rifle_squad" in us_ids

        # Spot-check: Polish faction gained 4 new para variants.
        pl_units = get_units_for_faction(Faction.POLISH)
        pl_new = {u.template_id for u in pl_units} & {
            "pl_para_commando",
            "pl_para_scout",
            "pl_para_sniper",
            "pl_para_heavy_weapons",
        }
        assert len(pl_new) == 4

    def test_get_cc2_units_lazy_cache_contains_new_variants(self):
        """The lazy cache returned by get_cc2_units() also exposes the new variants."""
        db = get_cc2_units()
        assert "us_sherman_easy_eight" in db
        assert "uk_commando_squad" in db
        assert "de_jagdpanther" in db
        assert "pl_para_sniper" in db
        # Second call returns the same cached object.
        assert get_cc2_units() is db

    def test_deployment_place_unit_accepts_new_vehicle_variant(self):
        """DeploymentPhase.place_unit() succeeds for a new vehicle variant ID."""
        config = DeploymentConfig(
            map_width=3,
            map_height=3,
            ally_zones=[[ZoneType.FRIENDLY for _ in range(3)] for _ in range(3)],
            axis_zones=[[ZoneType.FRIENDLY for _ in range(3)] for _ in range(3)],
            max_infantry=9,
            max_support=6,
            max_total=15,
        )
        phase = DeploymentPhase(config)
        phase.start_deployment(Faction.AMERICAN)

        # us_sherman_easy_eight is a TANK_MEDIUM (support slot) — should place.
        assert phase.place_unit("us_sherman_easy_eight", 0, 0) is True
        deployed = phase.get_deployed_positions(Faction.AMERICAN)
        assert deployed == [(0, 0, "us_sherman_easy_eight")]

    def test_deployment_place_unit_accepts_new_infantry_variant(self):
        """DeploymentPhase.place_unit() succeeds for a new faction variant ID."""
        config = DeploymentConfig(
            map_width=3,
            map_height=3,
            ally_zones=[[ZoneType.FRIENDLY for _ in range(3)] for _ in range(3)],
            axis_zones=[[ZoneType.FRIENDLY for _ in range(3)] for _ in range(3)],
            max_infantry=9,
            max_support=6,
            max_total=15,
        )
        phase = DeploymentPhase(config)
        phase.start_deployment(Faction.BRITISH)

        # uk_commando_squad is HEAVY_ASSAULT (infantry slot) — should place.
        assert phase.place_unit("uk_commando_squad", 1, 0) is True
        deployed = phase.get_deployed_positions(Faction.BRITISH)
        assert deployed == [(1, 0, "uk_commando_squad")]

    def test_deployment_rejects_unknown_template_id(self):
        """DeploymentPhase.place_unit() returns False for an unknown template_id."""
        config = DeploymentConfig(
            map_width=3,
            map_height=3,
            ally_zones=[[ZoneType.FRIENDLY for _ in range(3)] for _ in range(3)],
            axis_zones=[[ZoneType.FRIENDLY for _ in range(3)] for _ in range(3)],
        )
        phase = DeploymentPhase(config)
        phase.start_deployment(Faction.AMERICAN)
        assert phase.place_unit("nonexistent_template_xyz", 0, 0) is False


