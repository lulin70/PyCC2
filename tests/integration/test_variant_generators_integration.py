# Conflict handling — duplicate IDs are skipped with a warning log
# ============================================================================


class TestConflictHandling:
    """When a generator yields a template_id that already exists, it is skipped."""

    def test_duplicate_id_skipped_with_warning_log(self, caplog):
        """A generator-produced ID colliding with a factory entry is skipped + logged."""
        from pycc2.domain.systems import unit_database as db_module
        from pycc2.domain.systems.faction_variant_generator import (
            FactionVariantGenerator,
        )

        # Build a factory-only database (no generators yet) and inject one
        # duplicate ID so we can observe the skip behaviour deterministically.
        factory_db = build_cc2_unit_database()
        # Sanity: the ID we will duplicate must NOT already be a factory entry.
        assert "uk_commando_squad" in factory_db  # added by generator
        # Reset cache and rebuild, then tamper: pre-seed the dict with a
        # colliding entry before the generator runs. We simulate this by
        # monkey-patching FactionVariantGenerator.generate to yield a template
        # whose ID already exists in the factory database.
        colliding_template = CC2UnitTemplate(
            template_id="us_rifle_squad",  # existing factory ID
            display_name="Duplicate Rifle Squad",
            faction=Faction.AMERICAN,
            role=InfantryRole.RIFLE,
            weapon_primary_id="us_m1_garand",
        )
        original_generate = FactionVariantGenerator.generate

        def _patched_generate(self):
            # Return a list with one colliding + one unique template.
            return [
                colliding_template,
                CC2UnitTemplate(
                    template_id="test_unique_faction_variant",
                    display_name="Unique Test Variant",
                    faction=Faction.AMERICAN,
                    role=InfantryRole.RIFLE,
                    weapon_primary_id="us_m1_garand",
                ),
            ]

        FactionVariantGenerator.generate = _patched_generate  # type: ignore[method-assign]
        try:
            db_module.CC2_UNITS = {}
            with caplog.at_level(logging.WARNING, logger="pycc2.domain.systems.unit_database"):
                db = build_cc2_unit_database()
            # The colliding ID must still point at the FACTORY template,
            # not the duplicate.
            assert db["us_rifle_squad"].display_name != "Duplicate Rifle Squad"
            # The unique test variant was added.
            assert "test_unique_faction_variant" in db
            # A warning was logged for the collision.
            warning_messages = [r.message for r in caplog.records if r.levelno == logging.WARNING]
            assert any("Template ID conflict: us_rifle_squad" in msg for msg in warning_messages), (
                f"Expected conflict warning, got: {warning_messages}"
            )
        finally:
            FactionVariantGenerator.generate = original_generate  # type: ignore[method-assign]


